// Frame layout in sheet (8 cols × 6 rows = 48 slots)
// Row 0 (frames 0-7):  idle0, idle1, idle2, idle3, (empty...)
// Row 1 (frames 8-15): run0, run1, run2, run3, run4, run5, (empty...)
// Row 2 (frames 16-23): jump0, jump1, (empty...)
// Row 3 (frames 24-31): fall0, fall1, (empty...)
// Row 4 (frames 32-39): land0, land1, land2, (empty...)

export function createPlayerSpriteSheet(): SpriteSheet {
    const frames: (number[][] | null)[] = new Array(COLS * ROWS).fill(null)

    // Row 0: idle
    frames[0] = idle0
    frames[1] = idle1
    frames[2] = idle2
    frames[3] = idle3

    // Row 1: run
    frames[8] = run0
    frames[9] = run1
    frames[10] = run2
    frames[11] = run3
    frames[12] = run4
    frames[13] = run5

    // Row 2: jump
    frames[16] = jump0
    frames[17] = jump1

    // Row 3: fall
    frames[24] = fall0
    frames[25] = fall1

    // Row 4: land
    frames[32] = land0
    frames[33] = land1
    frames[34] = land2

    return createBitmapSheet(FRAME_W, FRAME_H, COLS, ROWS, palette, frames)
}

export function createPlayerAnimator(sheet: SpriteSheet): Animator {
    const animator = new Animator(sheet)

    const clips: AnimationClip[] = [
        { name: 'idle', frames: [0, 1, 2, 3], frameDuration: 0.22, loop: true },
        { name: 'run', frames: [8, 9, 10, 11, 12, 13], frameDuration: 0.09, loop: true },
        { name: 'jump', frames: [16, 17], frameDuration: 0.14, loop: false },
        { name: 'fall', frames: [24, 25], frameDuration: 0.16, loop: true },
        { name: 'land', frames: [32, 33, 34], frameDuration: 0.06, loop: false },
        { name: 'stop', frames: [34, 33, 32], frameDuration: 0.07, loop: false },
    ]

    for (const clip of clips) {
        animator.addClip(clip)
    }

    animator.play('idle')
    return animator
}
